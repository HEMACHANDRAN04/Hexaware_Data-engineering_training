def add(a,b):
    result=a+b
    print("Sum",result)

def sub(a,b):
    result=a-b
    print("subtraction",result)

def mul(a,b):
    result=a*b
    print("Multiplication",result)

def div(a,b):
    result=a/b
    print("division",result)

